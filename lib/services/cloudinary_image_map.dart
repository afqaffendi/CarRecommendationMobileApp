  // Get Cloudinary public ID for a car based on brand/model
  static String? getPublicId(String brand, String model, [String? variant]) {
    // Try exact matches first
    List<String> searchPatterns = [
      '$brand $model${variant != null ? ' $variant' : ''}',
      '$brand $model',
    ];
    
    for (String pattern in searchPatterns) {
      // Check main map
      if (_imageMap.containsKey(pattern)) {
        return _imageMap[pattern];
      }
      
      // Check alternative names
      if (_alternativeNames.containsKey(pattern)) {
        return _alternativeNames[pattern];
      }
      
      // Try case-insensitive search
      String? found = _findCaseInsensitive(pattern);
      if (found != null) return found;
    }
    
    return null;
  }

  static String? _findCaseInsensitive(String search) {
    String searchLower = search.toLowerCase();
    
    // Search in main map
    for (var entry in _imageMap.entries) {
      if (entry.key.toLowerCase() == searchLower) {
        return entry.value;
      }
    }
    
    // Search in alternatives
    for (var entry in _alternativeNames.entries) {
      if (entry.key.toLowerCase() == searchLower) {
        return entry.value;
      }
    }
    
    return null;
  }

  // Get all available car names (for debugging)
  static List<String> getAllAvailableNames() {
    return [..._imageMap.keys, ..._alternativeNames.keys]..sort();
  }

  // Check if image exists for car
  static bool hasImage(String brand, String model, [String? variant]) {
